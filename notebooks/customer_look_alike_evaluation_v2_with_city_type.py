import pandas as pd
import numpy as np
from sklearn.neighbors import NearestNeighbors
from sklearn.preprocessing import MinMaxScaler

from google.colab import auth
auth.authenticate_user()
print('Authenticated')
project_id = 'just-data-gci-dev'
from google.cloud import bigquery

client = bigquery.Client(project=project_id)



# Choose the campaign name:
campaign_name_parameter = {'campaign_name': 'pfo_customer_uplift_DENLGBES_1yearnopromo_fixed'}

# Choose below the look-alike conditions that you want to use:
# Options: country, optin, L7D_orders, L14D_orders, L30D_orders, L90D_orders, L180D_orders, L365D_orders, L730D_orders, L365D_AOV_cat


look_alike_conditions = ['country', 'city_type', 'optin', 'campaign_start_date', 'L14D_orders', 'L30D_orders', 'L90D_orders', 'L365D_orders', 'L365D_AOV_cat' ]

campaign_name = campaign_name_parameter['campaign_name']

query_countries_dates = f"""
    SELECT  DISTINCT country, campaign_start_date, city_type
     FROM `just-data-warehouse.customer_intelligence.customer_lookalike_evaluation_audience_citytype`
     WHERE campaign_name = '{campaign_name}'
     AND treatment = 1
     AND country = 'NL'
     AND campaign_start_date >= DATE('2024-09-22')
     ORDER BY 1, 2
     """

df_countries_dates = client.query(query_countries_dates).to_dataframe()


for index, row in df_countries_dates.iterrows():

    country = row['country']
    date = row['campaign_start_date']
    city_type = row['city_type']

    campaign_name = campaign_name_parameter['campaign_name']

    #Get order data from BigQuery at customer ID level

    query = f"""
    SELECT
    *,
    NTILE(3) OVER (PARTITION BY country ORDER BY L365D_AOV) AS L365D_AOV_cat
    FROM (
        SELECT  fo.customerid,
                lc.country,
                lc.city_type,
                CASE WHEN dcu.optin = true THEN 1 ELSE 0 END AS optin,
                lc.campaign_start_date,
                lc.campaign_end_date,
                '{campaign_name}' as campaign_name,
                lc.treatment,
                SUM(CASE WHEN DATE(orderdatetime) >= DATE_ADD(DATE(campaign_start_date), INTERVAL -7 DAY)  AND DATE(orderdatetime) < DATE(campaign_start_date) THEN nroforders ELSE 0 END) as L7D_orders,
                SUM(CASE WHEN DATE(orderdatetime) >= DATE_ADD(DATE(campaign_start_date), INTERVAL -14 DAY)  AND DATE(orderdatetime) < DATE(campaign_start_date) THEN nroforders ELSE 0 END) as L14D_orders,
                SUM(CASE WHEN DATE(orderdatetime) >= DATE_ADD(DATE(campaign_start_date), INTERVAL -30 DAY)  AND DATE(orderdatetime) < DATE(campaign_start_date) THEN nroforders ELSE 0 END) as L30D_orders,
                SUM(CASE WHEN DATE(orderdatetime) >= DATE_ADD(DATE(campaign_start_date), INTERVAL -30 DAY)  AND DATE(orderdatetime) < DATE(campaign_start_date) THEN CAST(ROUND(food_total) AS INT64) ELSE 0 END) as L30D_GMV,
                SUM(CASE WHEN DATE(orderdatetime) >= DATE_ADD(DATE(campaign_start_date), INTERVAL -90 DAY)  AND DATE(orderdatetime) < DATE(campaign_start_date) THEN nroforders ELSE 0 END) as L90D_orders,
                SUM(CASE WHEN DATE(orderdatetime) >= DATE_ADD(DATE(campaign_start_date), INTERVAL -180 DAY) AND DATE(orderdatetime) < DATE(campaign_start_date) THEN nroforders ELSE 0 END) as L180D_orders,
                SUM(CASE WHEN DATE(orderdatetime) >= DATE_ADD(DATE(campaign_start_date), INTERVAL -365 DAY) AND DATE(orderdatetime) < DATE(campaign_start_date) THEN nroforders ELSE 0 END) as L365D_orders,

                SAFE_DIVIDE(SUM(CASE WHEN DATE(orderdatetime) >= DATE_ADD(DATE(campaign_start_date), INTERVAL -365 DAY) AND DATE(orderdatetime) < DATE(campaign_start_date) THEN food_total ELSE 0 END),
                SUM(CASE WHEN DATE(orderdatetime) >= DATE_ADD(DATE(campaign_start_date), INTERVAL -365 DAY) AND DATE(orderdatetime) < DATE(campaign_start_date) THEN nroforders ELSE 0 END)) as L365D_AOV,

                SUM(CASE WHEN DATE(orderdatetime) > DATE(campaign_start_date) AND DATE(orderdatetime) <= DATE(campaign_end_date) THEN nroforders ELSE 0 END) as campaign_period_orders,
                SUM(CASE WHEN DATE(orderdatetime) > DATE(campaign_start_date) AND DATE(orderdatetime) <= DATE(campaign_end_date) THEN CAST(ROUND(restaurant_discount) AS INT64) ELSE 0 END) as campaign_period_discount,
                SUM(CASE WHEN DATE(orderdatetime) > DATE(campaign_start_date) AND DATE(orderdatetime) <= DATE(campaign_end_date) THEN CAST(ROUND(food_total) AS INT64) ELSE 0 END) as campaign_period_food_total

        FROM `just-data-warehouse.dwh.fact_order` as fo
                  INNER JOIN `just-data-warehouse.dwh.dim_customer` as dcu on fo.customerid = dcu.customerid
                  INNER JOIN `just-data-warehouse.dwh.dim_country` as dc on fo.countryid = dc.countryid
                  INNER JOIN `just-data-warehouse.customer_intelligence.customer_lookalike_evaluation_audience_citytype` as lc on fo.customerid = lc.customerid
                  INNER JOIN `just-data-warehouse.core_dwh.fact_order_margin` AS fom ON fo.orderid = fom.order_id
        WHERE DATE(orderdatetime) >= DATE_ADD(DATE(campaign_start_date), INTERVAL -365 DAY)
          AND DATE(orderdatetime) <= DATE('2025-05-01')
          AND DATE(orderdatetime) >= DATE('2023-01-01')
          AND campaign_name = '{campaign_name}'
          AND lc.country = '{country}'
          AND lc.city_type = '{city_type}'
          AND DATE(campaign_start_date) = '{date.strftime('%Y-%m-%d')}'
        GROUP BY 1, 2, 3, 4, 5, 6, 7, 8
        )
        """

    df = client.query(query).to_dataframe()
    print(f"There are {len(df)} customers in {date.strftime('%Y-%m-%d')} cohort in {country}")

    #Exclude outliers from data and create dataframes for treatment and lookalike customers

    treatment = df[
        (df['L7D_orders'] <= 7) &
        (df['L14D_orders'] <= 14) &
        (df['L30D_orders'] <= 30) &
        (df['L90D_orders'] <= 90) &
        (df['L180D_orders'] <= 180) &
        (df['L365D_orders'] <= 365) &
        (df['L365D_orders'] > 0) &
        (df['treatment'] == 1)
    ]

    lookalike = df[
        (df['L7D_orders'] <= 7) &
        (df['L14D_orders'] <= 14) &
        (df['L30D_orders'] <= 30) &
        (df['L90D_orders'] <= 90) &
        (df['L180D_orders'] <= 180) &
        (df['L365D_orders'] <= 365) &
        (df['L365D_orders'] > 0) &
        (df['treatment'] == 0)
    ]

    print(f"You excluded {len(df) - (len(treatment) + len(lookalike))} outliers from your audience")
    print(f"There are {len(treatment)} customers in your treatment group")
    print(f"There are {len(lookalike)} customers in your look-alike group")

    #Exact match of treatment with look-alikes

    # Merge the two DataFrames based on the specified columns
    merged_df = pd.merge(treatment, lookalike, on=look_alike_conditions, suffixes=('_treatment', '_lookalike'))

    merged_df = pd.merge(merged_df, lookalike[['customerid', 'L30D_orders', 'L30D_GMV']], left_on='customerid_lookalike', right_on='customerid', how='left', suffixes=('_treatment', '_lookalike'))

    # Rename L30D_orders columns:
    merged_df = merged_df.rename(columns={
        'L30D_orders_treatment': 'L30D_orders_treatment',  # Keep the original treatment name
        'L30D_orders_lookalike': 'L30D_orders_lookalike',  # Rename the joined lookalike column
        'L30D_GMV_treatment': 'L30D_GMV_treatment',  # Keep the original treatment name
        'L30D_GMV_lookalike': 'L30D_GMV_lookalike'  # Rename the joined lookalike column
    })

    # Create a new DataFrame aggreagated on customerid_treatment level take the average of campaign_period_orders_lookalike
    grouped_df = merged_df.groupby(['customerid_treatment', 'country', 'city_type', 'optin', 'campaign_start_date' , 'L30D_orders_treatment', 'L30D_GMV_treatment', 'campaign_period_orders_treatment', 'campaign_period_discount_treatment', 'campaign_period_food_total_treatment'])[['L30D_orders_lookalike', 'L30D_GMV_lookalike','campaign_period_orders_lookalike', 'campaign_period_discount_lookalike', 'campaign_period_food_total_lookalike']].mean().reset_index()

    print(f"You found an exact match for {len(grouped_df)} of {len(treatment)} customers in the treatment group")

    #Find the nearest neighbour of unmatched customers in treatment group

    # Get the customer IDs in treatment but not in grouped_df
    missing_customer_ids = set(treatment['customerid']) - set(grouped_df['customerid_treatment'])

    # Create a new DataFrame with the missing customer IDs
    unmatched_df = treatment[treatment['customerid'].isin(missing_customer_ids)]

    # Standardize columns in both DataFrames using MinMaxScaler
    scaler = MinMaxScaler()
    columns_to_scale = ['optin', 'L14D_orders', 'L30D_orders', 'L90D_orders', 'L180D_orders', 'L365D_orders', 'L365D_AOV_cat']

    lookalike_scaled = lookalike.copy()
    unmatched_scaled = unmatched_df.copy()

    lookalike_scaled[columns_to_scale] = scaler.fit_transform(lookalike[columns_to_scale])
    unmatched_scaled[columns_to_scale] = scaler.transform(unmatched_df[columns_to_scale])

    # Create an empty list to store the results for each country
    all_nearest_neighbors = []

    # Iterate over unique combinations of country and campaign_start_date
    for country in lookalike_scaled['country'].unique():
        for campaign_start_date in lookalike_scaled['campaign_start_date'].unique():
            # Filter data for the current country and campaign_start_date
            lookalike_subset = lookalike_scaled[
                (lookalike_scaled['country'] == country) &
                (lookalike_scaled['campaign_start_date'] == campaign_start_date)
            ]
            unmatched_subset = unmatched_scaled[
                (unmatched_scaled['country'] == country) &
                (unmatched_scaled['campaign_start_date'] == campaign_start_date)
            ]

            # Create and fit KNN model for the current subset
            knn = NearestNeighbors(n_neighbors=1)
            knn.fit(lookalike_subset[columns_to_scale])

            # Find nearest neighbors for unmatched customers in the current subset
            distances, indices = knn.kneighbors(unmatched_subset[columns_to_scale])

            # Create results DataFrame for the current subset
            nearest_neighbors_df = pd.DataFrame({
                'unmatched_customerid': unmatched_subset['customerid'],
                'country': country,
                'optin': unmatched_subset['optin'],
                'campaign_start_date': campaign_start_date,  # Add campaign_start_date to the results
                'unmatched_L30D_orders': unmatched_df.loc[unmatched_subset.index, 'L30D_orders'].values,  # Get original L30D_orders for treatment
                'unmatched_L30D_GMV': unmatched_df.loc[unmatched_subset.index, 'L30D_GMV'].values,  # Get original L30D_GMV for treatment
                'unmatched_campaign_period_orders': unmatched_subset['campaign_period_orders'],
                'unmatched_campaign_period_discount': unmatched_subset['campaign_period_discount'],
                'unmatched_campaign_period_food_total': unmatched_subset['campaign_period_food_total'],
                'nearest_neighbor_customerid': lookalike_subset['customerid'].iloc[indices.flatten()].values,
                'nearest_neighbor_L30D_orders': lookalike.loc[lookalike_subset['customerid'].iloc[indices.flatten()].index, 'L30D_orders'].values,  # Get original L30D_orders for lookalike
                'nearest_neighbor_L30D_GMV': lookalike.loc[lookalike_subset['customerid'].iloc[indices.flatten()].index, 'L30D_GMV'].values,  # Get original L30D_GMV for lookalike
                'nearest_neighbor_campaign_period_orders': lookalike_subset['campaign_period_orders'].iloc[indices.flatten()].values,
                'nearest_neighbor_campaign_period_discount': lookalike_subset['campaign_period_discount'].iloc[indices.flatten()].values,
                'nearest_neighbor_campaign_period_food_total': lookalike_subset['campaign_period_food_total'].iloc[indices.flatten()].values
            })

            # Append the results for the current subset to the list
            all_nearest_neighbors.append(nearest_neighbors_df)

    # Concatenate the results for all subsets into a single DataFrame
    final_nearest_neighbors = pd.concat(all_nearest_neighbors, ignore_index=True)

    print(f"{len(unmatched_df)} customers (with no look-alike) matched with their nearest neighbors")

    #Merge outputs of exact match and nearast neighbor dataframes

    # Rename columns in nearest_neighbors_df
    final_nearest_neighbors_df = final_nearest_neighbors.rename(columns={
        'unmatched_customerid': 'customerid_treatment',
        'country': 'country',
        'optin': 'optin',
        'campaign_start_date': 'campaign_start_date',
        'unmatched_L30D_orders': 'L30D_orders_treatment',
        'unmatched_L30D_GMV': 'L30D_GMV_treatment',
        'unmatched_campaign_period_orders': 'campaign_period_orders_treatment',
        'unmatched_campaign_period_discount': 'campaign_period_discount_treatment',
        'unmatched_campaign_period_food_total': 'campaign_period_food_total_treatment',
        'nearest_neighbor_L30D_orders': 'L30D_orders_lookalike',
        'nearest_neighbor_L30D_GMV': 'L30D_GMV_lookalike',
        'nearest_neighbor_campaign_period_orders': 'campaign_period_orders_lookalike',
        'nearest_neighbor_campaign_period_discount': 'campaign_period_discount_lookalike',
        'nearest_neighbor_campaign_period_food_total': 'campaign_period_food_total_lookalike',

    })

    # Select the desired columns from nearest_neighbors_df
    final_nearest_neighbors_df = final_nearest_neighbors_df[['customerid_treatment', 'country', 'optin', 'campaign_start_date', 'L30D_orders_treatment', 'L30D_GMV_treatment' ,'campaign_period_orders_treatment', 'L30D_orders_lookalike' , 'L30D_GMV_lookalike' , 'campaign_period_discount_treatment', 'campaign_period_food_total_treatment', 'campaign_period_orders_lookalike', 'campaign_period_discount_lookalike', 'campaign_period_food_total_lookalike']]

    # Append the two DataFrames
    grouped_df['source'] = 'exact_match'
    final_nearest_neighbors_df['source'] = 'nearest_neighbor'

    # Reset index to avoid potential conflicts during concatenation
    grouped_df = grouped_df.reset_index(drop=True)
    final_nearest_neighbors_df = final_nearest_neighbors_df.reset_index(drop=True)

    # Instead of ignore_index=True, reset index after concatenation
    final_df = pd.concat([grouped_df, final_nearest_neighbors_df], ignore_index=True) #add ignore_index=True to the pd.concat
    final_df = final_df.reset_index(drop=True)

    # Add campaign name and current timestamp
    final_df['campaign_name'] = df['campaign_name'].iloc[0]
    final_df['run_timestamp'] = pd.Timestamp.now(tz='Europe/Amsterdam')
    final_df = final_df.drop('city_type', axis=1)

    print(f"{len(final_df)} out of {len(treatment)} customers have at least 1 look-alike")

    #Write the output to BigQuery table

    # Define the table to insert into
    table_id = 'just-data-warehouse.customer_intelligence.customer_lookalike_evaluation_results_gmv2'

    # Insert the DataFrame into BigQuery
    job_config = bigquery.LoadJobConfig(
        # Specify a schema if the table doesn't exist or to enforce schema
        schema=[
            bigquery.SchemaField("customerid_treatment", "STRING"),
            bigquery.SchemaField("country", "STRING"),
            bigquery.SchemaField("optin", "FLOAT"),
            bigquery.SchemaField("campaign_name", "STRING"),
            bigquery.SchemaField("campaign_start_date", "DATE"),
            bigquery.SchemaField("L30D_orders_treatment", "FLOAT"),
            bigquery.SchemaField("L30D_GMV_treatment", "FLOAT"),
            bigquery.SchemaField("campaign_period_orders_treatment", "FLOAT"),
            bigquery.SchemaField("campaign_period_discount_treatment", "FLOAT"),
            bigquery.SchemaField("campaign_period_food_total_treatment", "FLOAT"),
            bigquery.SchemaField("L30D_orders_lookalike", "FLOAT"),
            bigquery.SchemaField("L30D_GMV_lookalike", "FLOAT"),
            bigquery.SchemaField("campaign_period_orders_lookalike", "FLOAT"),
            bigquery.SchemaField("campaign_period_discount_lookalike", "FLOAT"),
            bigquery.SchemaField("campaign_period_food_total_lookalike", "FLOAT"),
            bigquery.SchemaField("run_timestamp", "TIMESTAMP"),
            bigquery.SchemaField("source", "STRING"),

        ],
        write_disposition="WRITE_APPEND",  # Specify to append or overwrite data
    )

    job = client.load_table_from_dataframe(
        final_df, table_id, job_config=job_config
    )  # Make an API request.
    job.result()  # Wait for the job to complete.

    print(f"Loaded {len(final_df)} rows into {table_id}")


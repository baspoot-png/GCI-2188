def process_country_date(row_data):
    """
    The function to be executed in parallel for each row (country/date cohort).
    It receives data for a single row and performs all the processing and loading.
    """
    country = row_data['country']
    date = row_data['campaign_start_date']
    ntile_parameter = row_data['nrofbuckets']
    campaign_name = campaign_name_parameter['campaign_name']




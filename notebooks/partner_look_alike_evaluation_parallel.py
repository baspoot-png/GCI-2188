    matched_df = merged_df[['restaurantid_treatment', 'country','campaign_start_date' , 'L30D_orders_treatment' ,'campaign_period_orders_treatment', 'post_period_orders_treatment', 'restaurantid_lookalike', 'L30D_orders_lookalike' ,'campaign_period_orders_lookalike', 'post_period_orders_lookalike']]


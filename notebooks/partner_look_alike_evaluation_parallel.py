def main_multiprocess():

    # Prepare list of arguments (dictionaries for each row)
    tasks = df_countries_dates_buckets.to_dict('records')

    # --- Parallel Execution ---
    # Determine the number of workers. Colab free tier usually has 2 vCPUs.
    # We'll use os.cpu_count() or a sensible default.
    max_workers = os.cpu_count() or 10
    print(f"Starting parallel processing with {max_workers} processes...")

    # Use ProcessPoolExecutor to manage the processes
    multiprocessing.set_start_method('fork', force=True)
    with ProcessPoolExecutor(max_workers=max_workers) as executor:
        # map() submits all tasks and returns results in the order they were submitted
        results = list(executor.map(process_country_date, tasks))

    print("\n" + "=" * 40 + "\n")
    print(f"Combining {len(results)} result DataFrames...")

    start_time_concat = time.time()
    final_output_df = pd.concat(results, ignore_index=True)

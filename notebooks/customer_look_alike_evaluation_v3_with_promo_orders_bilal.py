query_results = f"""

    SELECT
      campaign_name,
      country,
      COUNT(DISTINCT customerid_treatment) AS nrofcustomers,
      COUNT(DISTINCT CASE WHEN source = 'exact_match' THEN customerid_treatment ELSE NULL END) AS nrofcustomers_exact_match,
      COUNT(DISTINCT CASE WHEN source = 'nearest_neighbor' THEN customerid_treatment ELSE NULL END) AS nrofcustomers_matched_nearest_neighbor,
      SAFE_DIVIDE(COUNT(DISTINCT CASE WHEN source = 'exact_match' THEN customerid_treatment ELSE NULL END), COUNT(DISTINCT customerid_treatment)) AS exact_match_coverage,
      SUM(L30D_orders_treatment) AS L30D_orders_treatment,
      SUM(L30D_orders_lookalike) AS L30D_orders_lookalike,
      SAFE_DIVIDE(SUM(L30D_orders_treatment), SUM(L30D_orders_lookalike)) - 1 AS pre_period_uplift,
      SUM(campaign_period_orders_treatment) AS campaign_period_orders_treatment,
      SUM(campaign_period_orders_lookalike) AS campaign_period_orders_lookalike,
      SAFE_DIVIDE(SUM(campaign_period_orders_treatment), SUM(campaign_period_orders_lookalike)) - 1 AS campaign_period_uplift,
      (SUM(campaign_period_orders_treatment) - SUM(campaign_period_orders_lookalike)) AS campaign_period_incr_orders,
      SUM(campaign_period_promo_orders_treatment) AS campaign_period_promo_orders_treatment,
      SUM(campaign_period_promo_orders_lookalike) AS campaign_period_promo_orders_lookalike,
      SAFE_DIVIDE((SUM(campaign_period_promo_orders_lookalike) / SUM(campaign_period_orders_lookalike)), (SUM(campaign_period_promo_orders_treatment) / SUM(campaign_period_orders_treatment))) AS incr_correction_factor,
      (SUM(campaign_period_orders_treatment) - SUM(campaign_period_orders_lookalike)) / (1 - SAFE_DIVIDE((SUM(campaign_period_promo_orders_lookalike) / SUM(campaign_period_orders_lookalike)), (SUM(campaign_period_promo_orders_treatment) / SUM(campaign_period_orders_treatment)))) AS corrected_incr_on_repeat_orders,
      (SAFE_DIVIDE(SUM(campaign_period_orders_treatment), SUM(campaign_period_orders_lookalike)) - 1) / (1 - SAFE_DIVIDE((SUM(campaign_period_promo_orders_lookalike) / SUM(campaign_period_orders_lookalike)), (SUM(campaign_period_promo_orders_treatment) / SUM(campaign_period_orders_treatment)))) AS corrected_order_uplift,

      COUNT(DISTINCT customerid_treatment) * ((SUM(campaign_period_orders_treatment) - SUM(campaign_period_orders_lookalike)) / (1 - SAFE_DIVIDE((SUM(campaign_period_promo_orders_lookalike) / SUM(campaign_period_orders_lookalike)), (SUM(campaign_period_promo_orders_treatment) / SUM(campaign_period_orders_treatment))))) / SUM(campaign_period_promo_orders_treatment) AS first_order_uplift,

      ((SUM(campaign_period_orders_treatment) - SUM(campaign_period_orders_lookalike)) / (1 - SAFE_DIVIDE((SUM(campaign_period_promo_orders_lookalike) / SUM(campaign_period_orders_lookalike)), (SUM(campaign_period_promo_orders_treatment) / SUM(campaign_period_orders_treatment))))) + (
      COUNT(DISTINCT customerid_treatment) * ((SUM(campaign_period_orders_treatment) - SUM(campaign_period_orders_lookalike)) / (1 - SAFE_DIVIDE((SUM(campaign_period_promo_orders_lookalike) / SUM(campaign_period_orders_lookalike)), (SUM(campaign_period_promo_orders_treatment) / SUM(campaign_period_orders_treatment))))) / SUM(campaign_period_promo_orders_treatment) ) AS total_incr_orders

    FROM `just-data-warehouse.customer_intelligence.customer_lookalike_evaluation_results_promo_orders`
    WHERE campaign_name = '{campaign_name}'
    GROUP BY 1, 2
    ORDER BY 1, 2

        """

